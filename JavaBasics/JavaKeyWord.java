/* 1. abstract: Java abstract keyword is used to declare abstract class.
 * Abstract class can provide the implementation of interface. It can have
 * abstract and non-abstract methods.
 *
 * 2. boolean: Java boolean keyword is used to declare a variable as a
 * boolean type. It can hold True and False values only.
 *
 * 3. break: Java break keyword is used to break loop or switch statement.
 * It breaks the current flow of the program at specified condition.
 *
 * 4. byte: Java byte keyword is used to declare a variable that can hold
 * an 8-bit data values.
 *
 * 5. case: Java case keyword is used to with the switch statements to
 * mark blocks of text.
 *
 * 6. catch: Java catch keyword is used to catch the exceptions generated
 * by try statements. It must be used after the try block only.
 *
 * 7. char: Java char keyword is used to declare a variable that can hold
 * unsigned 16-bit Unicode characters.
 *
 * 8. class: Java class keyword is used to declare a class.
 *
 * 9. continue: Java continue keyword is used to continue the loop. It
 * continues the current flow of the program and skips the remaining code
 * at the specified condition.
 *
 * 10. default: Java default keyword is used to specify the default block
 * of code in a switch statement.
 *
 * 11. do: Java do keyword is used in control statement to declare a loop.
 * It can iterate a part of the program several times.
 *
 * 12. double: Java double keyword is used to declare a variable that can
 * hold a 64-bit floating-point numbers.
 *
 * 13. else: Java else keyword is used to indicate the alternative
 * branches in an if statement.
 *
 * 14. enum: Java enum keyword is used to define a fixed set of constants.
 * Enum constructors are always private or default.
 *
 * 15. extends: Java extends keyword is used to indicate that a class is
 * derived from another class or interface.
 *
 * 16. final: Java final keyword is used to indicate that a variable holds
 * a constant value. It is applied with a variable. It is used to restrict
 * the user.
 *
 * 17. finally: Java finally keyword indicates a block of code in a
 * try-catch structure. This block is always executed whether exception is
 * handled or not.
 *
 * 18. float: Java float keyword is used to declare a variable that can
 * hold a 32-bit floating-point number.
 *
 * 19. for: Java for keyword is used to start a for loop. It is used to
 * execute a set of instructions/functions repeatedly when some conditions
 * become true. If the number of iteration is fixed, it is recommended to
 * use for loop.
 *
 * 20. if: Java if keyword tests the condition. It executes the if block
 * if condition is true.
 *
 * 21. implements: Java implements keyword is used to implement an
 * interface.
 *
 * 22. import: Java import keyword makes classes and interfaces available
 * and accessible to the current source code.
 *
 * 23. instanceof: Java instanceof keyword is used to test whether the
 * object is an instance of the specified class or implements an interface.
 *
 * 24. int: Java int keyword is used to declare a variable that can hold a
 * 32-bit signed integer.
 *
 * 25. interface: Java interface keyword is used to declare an interface. It
 * can have only abstract methods.
 *
 * 26. long: Java long keyword is used to declare a variable that can hold
 * a 64-bit integer.
 *
 * 27. native: Java native keyword is used to specify that a method is
 * implemented in native code using JNI (Java Native Interface).
 *
 * 28. new: Java new keyword is used to create new objects.
 *
 * 29. null: Java null keyword is used to indicate that a reference does not
 * refer to anything. It removes the garbage value.
 *
 * 30. package: Java package keyword is used to declare a Java package that
 * includes the classes.
 *
 * 31. private: Java private keyword is an access modifier. It is used to
 * indicate that a method or variable may be accessed only in the class in
 * which it is declared.
 *
 * 32. protected: Java protected keyword is an access modifier. It can be
 * accessible within package and outside the package but through inheritance
 * only. It can't be applied on the class.
 *
 * 33. public: Java public keyword is an access modifier. It is used to
 * indicate that an item is accessible anywhere. It has the widest scope
 * among all other modifiers.
 *
 * 34. return: Java return keyword is used to return from a method when its
 * execution is complete.
 *
 * 35. short: Java short keyword is used to declare a variable that can hold
 * a 16-bit integer.
 *
 * 36. static: Java static keyword is used to indicate that a variable or
 * method is a class method. The static keyword in Java is used for memory
 * management mainly.
 *
 * 37. strictfp: Java strictfp is used to restrict the floating-point
 * calculations to ensure portability.
 *
 * 38. super: Java super keyword is a reference variable that is used to refer
 * parent class object. It can be used to invoke immediate parent class method.
 *
 * 39. switch: The Java switch keyword contains a switch statement that
 * executes code based on test value. The switch statement tests the equality
 * of a variable against multiple values.
 *
 * 40. synchronized: Java synchronized keyword is used to specify the critical
 * sections or methods in multithreaded code.
 *
 * 41. this: Java this keyword can be used to refer the current object in a
 * method or constructor.
 *
 * 42. throw: The Java throw keyword is used to explicitly throw an exception.
 * The throw keyword is mainly used to throw custom exception. It is followed
 * by an instance.
 *
 * 43. throws: The Java throws keyword is used to declare an exception.
 * Checked exception can be propagated with throws.
 *
 * 44. transient: Java transient keyword is used in serialization. If you
 * define any data member as transient, it will not be serialized.
 *
 * 45. try: Java try keyword is used to start a block of code that will be
 * tested for exceptions. The try block must be followed by either catch or
 * finally block.
 *
 * 46. void: Java void keyword is used to specify that a method does not have
 * a return value.
 *
 * 47. volatile: Java volatile keyword is used to indicate that a variable may
 * change asynchronously.
 *
 * 48. while: Java while keyword is used to start a while loop. This loop
 * iterates a part of the program several times. If the number of iteration is
 * not fixed, it is recommended to use while loop.
 */
